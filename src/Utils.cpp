		lbl->setUserFlag("useful-gd-lists-mode"_spr, true);
		lbl->setUserObject("actual-hits"_spr, CCInteger::create(levelsTotal));
		lbl->setUserObject("displayed-hits"_spr, CCInteger::create(levelsSoFar));

