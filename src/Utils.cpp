	void parseColonResponse(const geode::utils::web::WebResponse& response) {
		Manager* manager = Manager::get();

		if (!response.ok() || response.code() != 200) {
			log::info("gdcolon.com request failed: {}", response.code());
			Manager::announceFailure("Response was not HTTP code 200.");
			return;
		}

		auto responseString = response.string();
		if (responseString.isErr()) {
			log::info("gdcolon.com response was not a string");
			Manager::announceFailure("Response was not a string!");
			return;
		}

		auto responseUnwrapped = responseString.unwrap();
		if (responseUnwrapped.empty()) {
			log::info("gdcolon.com response was a string, but unwrapping it failed.");
			Manager::announceFailure("Couldn't unwrap string!");
			return;
		}

		auto responseAsJSON = matjson::parse(responseString.unwrap());
		if (responseAsJSON.isErr()) {
			log::info("gdcolon.com response was a JSON string, but parsing it failed.");
			Manager::announceFailure("Couldn't JSON-ify string!");
			return;
		}

		auto responseAsJSONUnwrapped = responseAsJSON.unwrap();

		if (!responseAsJSONUnwrapped.contains("levels") || !responseAsJSONUnwrapped.contains("lists") || !responseAsJSONUnwrapped.contains("date")) {
			log::info("gdcolon.com response was a JSON string, but no relevant data found.");
			Manager::announceFailure("Unwrapped JSON-ified string, but no relevant data found!");
			return;
		}

		auto levelsArray = responseAsJSONUnwrapped["levels"].asArray();

		if (levelsArray.isErr()) {
			log::info("gdcolon.com response has relevant data, but parsing it failed.");
			Manager::announceFailure("Found levels, but couldn't parse it as an array!");
			return;
		}

		auto unwrappedLevels = levelsArray.unwrap();
		if (unwrappedLevels.empty()) {
			log::info("gdcolon.com response has relevant data, but there was nothing.");
			Manager::announceFailure("No levels were found! Either your internet sucks, Colon has changed how data is stored, or RobTop has abolished level lists.");
			return;
		}

		auto listsArray = responseAsJSONUnwrapped["lists"].asArray();

		if (listsArray.isErr()) {
			log::info("gdcolon.com response has relevant data, but parsing it failed.");
			Manager::announceFailure("Found level lists, but couldn't parse it as an array!");
			return;
		}

		auto unwrappedLists = listsArray.unwrap();
		if (unwrappedLists.empty()) {
			log::info("gdcolon.com response has relevant data, but there was nothing.");
			Manager::announceFailure("No level lists were found! Either your internet sucks, Colon has changed how data is stored, or RobTop has abolished level lists.");
			return;
		}

		auto date = responseAsJSONUnwrapped["date"].asInt();

		if (date.isErr()) {
			log::info("gdcolon.com response has relevant data, but parsing it failed.");
			Manager::announceFailure("Found a date, but couldn't parse it as an integer!");
			return;
		}

		if (date.unwrapOr(-1) == manager->date) {
			log::info("gdcolon.com response has relevant data, everything parses correctly, but there is no need to update anything as the date has not changed.");
			Notification::create("No new data found.", NotificationIcon::Info, 1.5f)->show();
			return;
		}

		manager->date = -1;
		manager->yeahDontEvenBother = false;
		if (!manager->completedLevelIDs.empty()) manager->completedLevelIDs.clear();
		if (!manager->completedListIDs.empty()) manager->completedListIDs.clear();
		if (!manager->levelIDInfoMap.empty()) manager->levelIDInfoMap.clear();
		if (!manager->listIDInfoList.empty()) manager->listIDInfoList.clear();

		manager->date = date.unwrapOr(-1);

		for (const matjson::Value& levelEntry : unwrappedLevels) {
			if (!levelEntry.contains("id") || !levelEntry.contains("difficultyIndex") || !levelEntry.contains("stars") || !levelEntry.contains("lists")) continue;

			auto attemptedLevelIDString = levelEntry["id"].asString();
			if (attemptedLevelIDString.isErr()) continue;

			auto difficultyIndex = levelEntry["difficultyIndex"].asInt().unwrapOr(-1);
			if (difficultyIndex < 0) continue;

			auto stars = levelEntry["stars"].asInt().unwrapOr(-1);
			if (stars < 0) continue;

			auto attemptedListArray = levelEntry["lists"].asArray();
			if (attemptedListArray.isErr()) continue;

			auto idStr = attemptedLevelIDString.unwrap();
			if (idStr.empty()) continue;

			auto levelID = geode::utils::numFromString<int>(idStr).unwrapOr(-1);
			if (levelID < 1) continue;

			auto lists = attemptedListArray.unwrap();
			if (lists.empty()) continue;

			std::vector<int> listIDs {};

			for (const matjson::Value& listIDEntry : lists) {
				auto listIDInt = listIDEntry.asInt().unwrapOr(-1);
				if (listIDInt < 1) continue;

				listIDs.push_back(listIDInt);
			}

			const size_t numberOfLists = listIDs.size();

			manager->levelIDInfoMap[levelID] = UsefulLevel {
				.difficulty = difficultyIndex,
				.starCount = stars,
				.numberOfLists = numberOfLists,
				.listIDs = std::move(listIDs)
			};
		}

		for (const matjson::Value& listEntry : unwrappedLists) {
			if (!listEntry.contains("id") || !listEntry.contains("name") || !listEntry.contains("author") || !listEntry.contains("levels") || !listEntry.contains("difficulty") || !listEntry.contains("diamonds") || !listEntry.contains("levelsRequired")) continue;

			auto attemptedListIDString = listEntry["id"].asString();
			if (attemptedListIDString.isErr()) continue;

			auto name = listEntry["name"].asString();
			if (name.isErr() || name.unwrap().empty()) continue;

			auto author = listEntry["author"].asString();
			if (author.isErr() || author.unwrap().empty()) continue;

			auto attemptedLevelArray = listEntry["levels"].asArray();
			if (attemptedLevelArray.isErr()) continue;

			auto difficulty = listEntry["difficulty"].asString();
			if (difficulty.isErr() || difficulty.unwrap().empty()) continue;

			auto diamonds = listEntry["diamonds"].asInt().unwrapOr(-1);
			if (diamonds < 0) continue;

			auto levelsRequired = listEntry["levelsRequired"].asInt().unwrapOr(-1);
			if (levelsRequired < 0) continue;

			auto idStr = attemptedListIDString.unwrap();
			if (idStr.empty()) continue;

			auto listID = geode::utils::numFromString<int>(idStr).unwrapOr(-1);
			if (listID < 1) continue;
			log::info("parsed listID: {}", listID);

			if (GameLevelManager::get()->getSavedLevelList(static_cast<int>(listID)) && GameStatsManager::get()->hasClaimedListReward(GameLevelManager::get()->getSavedLevelList(static_cast<int>(listID)))) manager->completedListIDs.push_back(listID);

			auto levels = attemptedLevelArray.unwrap();
			if (levels.empty()) continue;

			std::vector<int> levelIDs {};

			for (const matjson::Value& levelIDEntry : levels) {
				const auto attemptedString = levelIDEntry.asString();
				if (attemptedString.isErr()) continue;

				auto s = attemptedString.unwrap();
				if (s.empty()) continue;

				auto n = geode::utils::numFromString<int>(s).unwrapOr(-1);
				if (n < 1) continue;

				levelIDs.push_back(n);
			}

			const size_t numberOfLevels = levelIDs.size();
			const std::string& unwrappedDifficulty = difficulty.unwrap();

			manager->listIDInfoList.push_back(
				UsefulList {
					.listID = listID,
					.name = name.unwrap(),
					.author = author.unwrap(),
					.difficulty = Manager::wellGeodeSDKStringSettingsAreReallyLackingIMO(unwrappedDifficulty),
					.difficultyIconKey = Manager::wellColonNeverGaveDifficultyIndiciesToTheLevelListsSoHereWeAre(unwrappedDifficulty),
					.difficultyIconColor = Manager::wellColonNeverGaveDifficultyIndiciesToTheLevelListsSoHereWeAreForColors(unwrappedDifficulty),
					.diamonds = diamonds,
					.levelsRequired = levelsRequired,
					.numberOfLevels = numberOfLevels,
					.levelIDs = std::move(levelIDs)
				}
			);
		}

		for (GJGameLevel* level : CCArrayExt<GJGameLevel*>(GameLevelManager::get()->getCompletedLevels(false))) {
			if (level) manager->completedLevelIDs.push_back(level->m_levelID.value());
		}

		log::info("manager->date: {}", manager->date);
		log::info("manager->yeahDontEvenBother: {}", manager->yeahDontEvenBother);
		log::info("manager->completedLevelIDs: {}", manager->completedLevelIDs.size());
		log::info("manager->completedListIDs: {}", manager->completedListIDs.size());
		log::info("manager->levelIDsToLists: {}", manager->levelIDInfoMap.size());
		log::info("manager->listsToLevelIDs: {}", manager->listIDInfoList.size());
	}

	void showUsefulLevelInfo(GJGameLevel* level) {
		if (!level) return;

		Manager* manager = Manager::get();
		if (manager->yeahDontEvenBother) return;

		const int levelIDBeingInvestigated = level->m_levelID.value();
		auto& completedLevels = manager->completedLevelIDs;
		auto& levelIDInfoMap = manager->levelIDInfoMap;
		auto& listIDInfoMap = manager->listIDInfoList;

		if (!levelIDInfoMap.contains(levelIDBeingInvestigated)) {
			FLAlertLayer* ohNo = FLAlertLayer::create("Oh no!", "This level doesn't seem to be useful for level list grinding at all!", "Aww man...");
			Utils::makeUndraggable(ohNo);
			ohNo->show();
			return;
		}

		const auto&[difficulty, starCount, numberOfLists, listIDs] = levelIDInfoMap[levelIDBeingInvestigated];
		const size_t numLists = numberOfLists;
		std::string listOfLevels = "";
		int claimableDiamonds = 0;
		intmax_t numDiamonds = 0;
		int completedLists = 0;
		int claimableLists = 0;

		for (int listID : listIDs) {
			if (listID < 0 || listIDInfoMap.size() < listID) continue;

			const UsefulList& listEntry = listIDInfoMap.at(listID);
			geode::Ref<GJLevelList> listRef = manager->levelLists[listEntry.listID];
			GJLevelList* levelList = listRef.data();

			manager->listIDsForIteration.push_back(levelList->m_listID);
			manager->seenInTheseLists.push_back(listRef);

			const int completedLevelsCount = levelList->completedLevels();
			const int incompleteLevelsFromListID = std::min<int>(levelList->m_levelsToClaim - completedLevelsCount, levelList->m_levelsToClaim);
			const int clampedIncompleteLevels = std::clamp<int>(incompleteLevelsFromListID, 0, levelList->m_levelsToClaim);

			if (incompleteLevelsFromListID < 1) completedLists++;
			numDiamonds += listEntry.diamonds;

			listOfLevels += fmt::format(
				"- ![{}](frame:diffIcon_{}_btn_001.png?scale={}) <c-{}>{}</c> by <co>{}</c> "
				"(<cl>{}</c> ![diamonds](frame:GJ_diamondsIcon_001.png?scale=0.5)",
				listEntry.difficultyIconKey, listEntry.difficultyIconKey, listEntry.difficulty != 10 ? "0.3" : "0.25",
				listEntry.difficultyIconColor, listEntry.name, listEntry.author, listEntry.diamonds
			);

			if (incompleteLevelsFromListID < 1 && completedLevelsCount >= levelList->m_levelsToClaim) {
				listOfLevels += ") <cg>[![complete](frame:GJ_completesIcon_001.png?scale=0.35)]</c>";
				if (!GameStatsManager::get()->hasClaimedListReward(levelList)) {
					claimableLists++;
					claimableDiamonds += levelList->m_diamonds;
				}
			} else {
				listOfLevels += fmt::format(
					") [{} level{} left]",
					clampedIncompleteLevels, clampedIncompleteLevels != 1 ? "s" : ""
				);
			}
			listOfLevels += "\n";
		}

		FLAlertLayer* info {};

		if (completedLists != numberOfLists || (completedLists == numberOfLists && claimableLists > 0)) {
			const std::string& disclaimer = completedLists > 0 ?
				fmt::format(
					" (<cg>{}</c> completed, <co>{}</c> remaining)",
					completedLists, std::abs(numLists - completedLists)
				) : "";
			const std::string& completion = std::ranges::find(completedLevels.begin(), completedLevels.end(), levelIDBeingInvestigated) == completedLevels.end() ?
				fmt::format(
					"Completing this level will bring you closer to getting "
	 				"<cl>{}</c> ![diamonds](frame:GJ_diamondsIcon_001.png?scale=0.5) total.",
					numDiamonds
				) : fmt::format(
					"<co>***You've already completed this level!***</c> "
					"Beat other levels in these lists to get <cl>{}</c> "
	 				"![diamonds](frame:GJ_diamondsIcon_001.png?scale=0.5)!",
					numDiamonds
				);
			const std::string& claimNow = claimableLists > 0 ?
				fmt::format(
					"\nOf these lists, <co>{} list{} {} claimable</c> for "
					"<cl>{}</c> ![diamonds](frame:GJ_diamondsIcon_001.png?scale=0.5)!",
					claimableLists, claimableLists != 1 ? "s" : "",
					claimableLists != 1 ? "are" : "is", claimableDiamonds
				) : "";
			info = MDPopup::create(
				static_cast<std::string>(level->m_levelName),
				fmt::format(
					"This level exists in <cl>{}</c> lists{}:\n{}{}\n{}",
					numLists, disclaimer, listOfLevels, claimNow, completion
				), "Alright!", manager->preventRecursion ? nullptr : (manager->openAsLevelLists ? "Go To Lists" : "Show Levels"), [](const bool buttonTwo) {
					Manager* managerLambda = Manager::get();
					if (!buttonTwo || !managerLambda->enabled || managerLambda->yeahDontEvenBother) {
						managerLambda->seenInTheseLists.clear();
						managerLambda->listIDsForIteration.clear();
						return;
					}

					if (managerLambda->openAsLevelLists) Utils::showTheLists();
					else Utils::showTheLevelsInsideTheLists();

					managerLambda->preventRecursion = true;

					managerLambda->seenInTheseLists.clear();
					managerLambda->listIDsForIteration.clear();
				}
			);
		} else {
			manager->seenInTheseLists.clear();
			manager->listIDsForIteration.clear();

			info = MDPopup::create(
				static_cast<std::string>(level->m_levelName),
				fmt::format(
					"This level exists in <cl>{}</c> lists:\n{}\n"
					"<cg>***You've already claimed all the***</c> <cl>***{}***</c> ![diamonds](frame:GJ_diamondsIcon_001.png?scale=0.5) <cg>***from these lists! Nice job!***</c>",
					numLists, listOfLevels, numDiamonds
				), "Nice!"
			);
		}

		info->setUserFlag("undefined0.draggable-popups/undraggable-popup", true);
		info->m_noElasticity = true;
		info->show();
	}

	void showTheLists() {
		GJSearchObject* gjsoTemp = GJSearchObject::create(static_cast<SearchType>(651212));
		gjsoTemp->m_searchMode = 1;

		CCArray* lists = CCArray::create();
		for (geode::Ref<GJLevelList> levelListRef : Manager::get()->seenInTheseLists) {
			GJLevelList* lvlLst = levelListRef.data();
			if (!lvlLst) continue;
			lists->addObject(lvlLst);
		}
		GameLevelManager::sharedState()->storeSearchResult(lists, fmt::format("{}:{}:{}", lists->count(), 0, lists->count()), gjsoTemp->getKey());

		CCScene* lblScene = LevelBrowserLayer::scene(gjsoTemp);

		lblScene->addCleanupCallback([]() {
			Manager::get()->preventRecursion = false;
		});

		CCTransitionFade* transitionFade = CCTransitionFade::create(.5f, lblScene);
		Utils::removeRefreshButtonAndBetterInfoStarButton(lblScene->getChildByType<LevelBrowserLayer>(0));

		CCDirector::sharedDirector()->pushScene(transitionFade);
	}


