	void doTheCountdown(FLAlertLayer* theAlert) {
		if (!theAlert) return;

		if (theAlert->m_mainLayer) theAlert->m_mainLayer->setScale(.85f);

		constexpr float countdownLength = 15.f;

		if (theAlert->m_button2) {
			const float origScale = theAlert->m_button2->getParent()->getScale();
			theAlert->m_button2->getParent()->setScale(0.f);
			theAlert->m_button2->getParent()->runAction(CCSequence::create(CCDelayTime::create(countdownLength), CCScaleTo::create(0.f, origScale), nullptr));
		}
		const float origScaleToo = theAlert->m_button1->getParent()->getScale();
		theAlert->m_button1->getParent()->setScale(0.f);
		theAlert->m_button1->getParent()->runAction(CCSequence::create(CCDelayTime::create(countdownLength), CCScaleTo::create(0.f, origScaleToo), nullptr));

		if (theAlert->m_buttonMenu->getParent()) {
			CCLabelBMFont* label = CCLabelBMFont::create(fmt::format("STOP AND READ!!! ({})", static_cast<int>(countdownLength)).c_str(), "bigFont.fnt");
			theAlert->m_buttonMenu->getParent()->addChild(label);
			label->setZOrder(theAlert->m_buttonMenu->getZOrder() + 1);
			label->setPosition(theAlert->m_buttonMenu->getPosition());
			label->setColor({255, 0, 0});
			label->runAction(CCSequence::create(CCTintTo::create(countdownLength, 0, 255, 0), CCRemoveSelf::create(), nullptr));
			CCArray* actions = CCArray::create();
			for (int i = 0; i < static_cast<int>(countdownLength); ++i) {
				actions->addObject(CCScaleTo::create(0.f, 1.5f));
				actions->addObject(CCEaseExponentialOut::create(CCScaleTo::create(1.f, 1.f)));
			}
			CCArray* moreActions = CCArray::create();
			for (int j = static_cast<int>(countdownLength); j > 0; --j) {
				moreActions->addObject(CCLabelSetString::create(1.f, fmt::format("STOP AND READ!!! ({})", j).c_str()));
			}
			label->runAction(CCSequence::create(actions));
			label->runAction(CCSequence::create(moreActions));
		}
	}

